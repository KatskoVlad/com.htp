public class CalculatePriceCar {
    public static double sumPriceCar(double priceCar, double priceKpp, double priceEngine, double priceWhiles, double priceKuzov, double priceKopot){
        return priceCar + priceKpp + priceEngine + priceWhiles + priceKuzov + priceKopot;
    }
}
